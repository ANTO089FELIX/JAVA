import java.util.*;
class pattern
{
    public static void main(String[]args)
    {
        int i,j,k,row;
        System.out.println("enter the pattern row size");
        Scanner d=new Scanner(System.in);
        row=d.nextInt();
        for(i=0;i<=row;i++)
        {
            for(j=0;j<=row-1;j++)
            {
                System.out.println(" ");
            }
            for(j=0;j<=row-1;j++)
            {
                System.out.println("*");
            }
        }
    }
}
